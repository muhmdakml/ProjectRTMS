		if (Bomb1 == 0 && Bomb2 == 0) keyin = 0;
		
		seg_display(i);		// Show countdown timer on 7 segment (2 block)	
		i--;				// Countdown per second
		if (i< 0) break;	// Break WHILE Loop if countdown has finished
